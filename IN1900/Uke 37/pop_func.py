import math as m


def N_of_t(B, k, t, C):
    # Returns N(t)
    return (B/(1 + C*m.e**(-(k*t))))


for i in range(1, 49):
    print(f"t = {i:2d} : {N_of_t(50000, 0.2, i, 9):.3f}")


